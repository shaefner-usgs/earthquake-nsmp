<p>After a major earthquake it is imperative to check the condition of structures
  in order to be able to assess their structural integrity for public safety.</p>

<p>Recordings from structures instrumented with sensors help engineers assess
  the structure&rsquo;s safety and functionality in a timely fashion. Such
  recordings are also critical to designing safer infrastructure, and to
  preventing loss of life.</p>

<p>In particular, monitoring structures helps engineers to:</p>

<ul>
  <li>assess a structure&rsquo;s safety immediately following a damaging quake,</li>
  <li>understand how damage to structural and nonstructural components (such
    as equipment and pipes) occurs from strong shaking,</li>
  <li>evaluate and improve earthquake-resistant design techniques and also
    methods for predicting the seismic performance of structures, and</li>
  <li>improve earthquake-resistant design provisions in seismic codes.</li>
</ul>

<p>More than 250 structures throughout the United States have been
  outfitted with seismic sensors by the USGS National Strong Motion Project
  (NSMP) to improve the overall understanding of earthquakes and their effects
  on the built environment.</p>
<p>
  The <a href="evolution.php">instrumentation and monitoring of structures</a> by NSMP is only one part of
  USGS efforts to protect people&rsquo;s lives and property from earthquake
  hazards in all of the Nation&rsquo;s seismically active regions.
</p>

