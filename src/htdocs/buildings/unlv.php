<p>The Science and Engineering Building at the University of Nevada Las Vegas
  (UNLV) campus has been recently instrumented for earthquake monitoring by the
  National Strong Motion Project (NSMP) of the U.S. Geological Survey (USGS) in
  collaboration with the UNLV&rsquo;s Department of Civil and Environmental
  Engineering and Construction. This is the first building in Las Vegas
  furnished with a sophisticated seismic monitoring system to monitor its
  structural integrity in near real-time. The monitoring systems, combining
  sensing technologies with an on-site computer, show how earthquakes affect
  the structure, providing invaluable information that can be used to judge
  the safety of the building after an event and to design safer structures in
  the future.</p>

<div class="figure">
  <img src="img/unlv-exterior_2x.jpg" alt="Photo of the UNLV Building" />
