<p>The UNLV Science and Engineering Building instrumentation is more unique than
  any building instrumented before. This particular structure has been awarded
  the Silver rating by the Leadership in Energy and Environmental Design (LEED)
  program. In order to obtain a LEED rating, the building must meet
  environmentally responsible and sustainable design, construction, and
  operation standards.</p>

<p>This is the first environmentally friendly building instrumented by the NSMP,
  and the only building monitored for earthquake shaking in Las Vegas. The
  intention of instrumenting this particular steel building is to see how
  buildings that are designed with the environment in mind, hold up to the
  strong lateral forces created by earthquakes. It is intended to use the
  obtained data from the sensors and the building to improve on environmentally
  friendly designs.</p>

<h2>USGS Uses Advanced Sensing Technologies</h2>

