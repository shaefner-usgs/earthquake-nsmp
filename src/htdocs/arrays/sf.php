<h2>Court of Appeals Building in San Francisco</h2>

<figure class="right">
  <img src="../img/sf_court.gif" alt="SF court bulding" style="max-width: 300px;" />
</figure>

<p>The historical Court of Appeals Building in San Francisco, California
has been recently retrofitted by a base-isolation system. During the retrofit, the
U.S. Geological Survey with the cooperation of the U.S. General Services Administration
installed a total of 36 channels of accelerometers with state-of-the-art digital recording
capability (operational as of 1998). During future earthquakes, the dense array of
recorders will provide vital data on the performance of the isolators and the response of
the superstructure. A free-field station has been deployed close to the building which
will provide key input data for evaluation of structures in the proximity of the Court of
Appeals Building and will contribute to the assessment of the variation of ground motion
within downtown San Francisco.</p>

<h2 class="clear">McKelvey Building, USGS Menlo Park Campus</h2>

<figure class="right">
  <img src="../img/mckelvey.gif" alt="McKelvey building" style="max-width: 300px;" />
</figure>

<p>The second building that is cooperatively instrumented is the three-story (two
plus basement) McKelvey Building on the USGS Western Regional Campus in Menlo Park,
California. The construction of this building had started when the 1994
Northridge earthquake occurred causing damages to column-beam joints of many steel
buildings. The design engineers for the McKelvey Building revised the steel connections
during the construction process. The 11-channel array of recorders in the building is
designed to provide translational, torsional and pounding response information of the
building using digital recording capability. Also, within close proximity, is a free-field
station that will provide important information related to the variation of motions within
the vicinity of the USGS campus.</p>

For further information on the building instrumentation, contact <a href="mailto:celebi@usgs.gov">Mehmet Celebi</a>.
