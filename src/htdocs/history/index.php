<p>Strong-motion recording in the United States has roots in the World
  Engineering Congress that convened in Tokyo in 1929. American engineers
  returned from these meetings convinced there was an immediate need for the
  United States to develop a rugged seismograph able to record potentially
  damaging ground motions and to monitor the response of critical structures
  during strong local earthquakes. In 1931, Congress allocated additional funds
  to the U.S. Department of Commerce, Coast and Geodetic Survey (C&amp;GS) for
  establishment of an engineering seismology program, including the development
  of a strong-motion seismograph (accelerograph), and the implementation and
  operation of a national strong-motion network. The first accelerograph, a
  modification of the Wood-Anderson seismograph, was designed by the National
  Bureau of Standards in the Department of Commerce. In 1932, the C&amp;GS set
  up the Seismological Field Survey in California with headquarters in San
  Francisco.</p>

<p>The first U.S. accelerographs were installed in southern California in the
  summer of 1932.  Following the successful recording of strong-ground shaking
  during the 1933 Long Beach earthquake, the network was expanded to more than
  50 instruments in the western United States including some at the upper levels
  of buildings in the Los Angeles and San Francisco areas.  The C&amp;GS
  operated all strong-motion instrumentation in the United States regardless of
  ownership, but in 1963 the first &ldquo;modern&rdquo; accelerograph was
  developed leading to the establishment of instrumentation programs by many
  other organizations. Network responsibilities were transferred to the National
  Oceanic and Atmospheric Administration&rsquo;s earthquake program in 1970.
  By 1972 the network included 575 accelerographs at permanent stations located
  throughout the United States and in Central and South America.  In 1973 (with
  National Science Foundation funding), the entire strong-motion program was
  absorbed by the U.S. Department of the Interior, U.S. Geological Survey, as
  part of the National Earthquake Hazards Reduction Program.</p>

<p>The NSMP and it&rsquo;s predecessors have evolved over more than eight
  decades.  At times some 1200 stations have participated in the National
  Strong-Motion Network (NSMN). The NSMP currently operates over 1,000
  strong-motion instruments at over 700 permanent <a href="../stations.php">stations</a>
  located in <a href="states.php">33 States and the Caribbean</a>.  The NSMN is
  primarily the result of <a href="cooperators.php">cooperative efforts</a> with
  many other Federal, State, and local agencies, private companies, and academic
  institutions.</p>

<p>The NSMP is part of the USGS <a href="/">Earthquake Hazards Program</a>, and
  its main campus location which is at the <a href="https://www.usgs.gov/centers/earthquake-science-center">Earthquake Science Center</a>
  in northern California.  NSMN Operations activities are coordinated with a
  <a href="https://www.usgs.gov/centers/earthquake-science-center/pasadena-field-office">field office in Pasadena, California</a>.</p>
